#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

int	main(void)
{
	int	x;
	int	y;

	x = 42;
	y = 24;
	ft_swap(&x, &y);
	printf("%d", x);
	printf("%d", y);
}
