#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	nb;
	int	sign;

	i = 0;
	nb = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] <= 13 && str[i] >= 9))
	{
		i++;
	}
	while ((str[i] == 45 || str[i] == 43) && str[i])
	{	
		if (str[i] == 45)
		{
			sign *= -1;
		}
	i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
	nb = (nb * 10) + (str[i] - 48);
		i++;
	}
	return (nb * sign);
}
