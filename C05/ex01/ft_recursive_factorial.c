int	ft_recursive_factorial(int nb)
{
	int	c;

	c = 1;
	if (nb > 0)
	{
		c = nb * ft_recursive_factorial(nb - 1);
		nb --;
	}
	if (nb < 0)
		return (0);
	return (c);
}
