#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb == 1)
		return (1);
	while (i * i <= nb)
	{
		if (nb == i * i)
		{
			return (i);
		}
	i++;
	}
	return (0);
}
